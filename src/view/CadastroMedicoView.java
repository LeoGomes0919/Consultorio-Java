    private void jcb_medicoDialogPopupMenuWillBecomeInvisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_jcb_medicoDialogPopupMenuWillBecomeInvisible
        if (jcb_medicoDialog.isPopupVisible()) {
            try {
                String nome = (String) jcb_medicoDialog.getSelectedItem();

                //recuperar dados do banco
                medico = medicoCtrl.retoMedicoNomeCtrl(nome);

                //setar na interface
                if (txt_usuarioDialog.getText().equals("null") && txt_senhaDialog.getText().equals("null")) {
                    txt_usuarioDialog.setText("");
                    txt_senhaDialog.setText("");
                } else {
                    txt_usuarioDialog.setText(medico.getUsuario());
                    txt_senhaDialog.setText(medico.getSenha());
                }
                jcb_permissaoDialog.setSelectedItem(medico.getPermissao());
                jDialogo_Menu.setVisible(true);
                habilitarDesabiliar(true);
                preencherMedicoComboBox();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro ao cadastrar usuário!", "AVISO", JOptionPane.ERROR_MESSAGE);
                this.habilitarDesabiliar(false);
            }
        }
    }//GEN-LAST:event_jcb_medicoDialogPopupMenuWillBecomeInvisible

