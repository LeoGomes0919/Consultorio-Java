    private void preencherMedicoComboBox() {
        if (jcb_medicoDialog.getItemCount() == 0) {
            jcb_medicoDialog.setSelectedItem("Sem Registro");
        } else {
            medico = medicoCtrl.retoMedicoNomeCtrl(jcb_medicoDialog.getSelectedItem().toString());
            txt_codigoDialog.setText(String.valueOf(medico.getIdMedico()));
        }
    }

    private void carregarMedico() {
        listarMedico = medicoCtrl.retoListMedicoCrtl();
        jcb_medicoDialog.removeAllItems();
        for (int i = 0; i < listarMedico.size(); i++) {
            jcb_medicoDialog.addItem(listarMedico.get(i).getNome());
        }
    }

